#include "util.h"
#include <stdio.h>
#include <stdlib.h>
#include <sys/types.h>
#include <sys/stat.h>
#include <fcntl.h>
#include <dirent.h>
#include <unistd.h>


void list(char *directory) {
    DIR *dir;

    if(!is_dir(*directory)) {
        print_fileinfo(*directory);
        return;
    }

    dir = opendir(*directory);

    closedir(dir);

}

int main(int argc, char *argv[]){
    for(int i = 1; i < argc; i++){
        list(argv[i]);
    }

    return 0;
}